import { useState, useEffect } from 'react';
import { useNavigate, Link } from 'react-router-dom';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Mail, Lock, Eye, EyeOff } from 'lucide-react';
import { useToast } from '@/hooks/use-toast';
import { signIn, signInWithGoogle, resetPassword } from '@/firebase/auth';
import { useAuth } from '@/contexts/AuthContext';
import logo from '@/assets/logo.png';

export default function Login() {
  const navigate = useNavigate();
  const { toast } = useToast();
  const { currentUser, shopId, refreshUser } = useAuth();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [isLoading, setIsLoading] = useState(false);

  // Redirect if already logged in
  useEffect(() => {
    console.log('Login useEffect - currentUser:', currentUser);
    console.log('Login useEffect - shopId:', shopId);
    console.log('Login useEffect - userRole:', currentUser?.role);
    
    if (currentUser) {
      // Check for SUPER_ADMIN first - they don't need a shop
      if (currentUser.role === 'SUPER_ADMIN') {
        console.log('Redirecting SUPER_ADMIN to admin dashboard...');
        navigate('/admin');
        return;
      }
      
      if (shopId) {
        // User has a shop, go to dashboard
        console.log('Redirecting to dashboard...');
        navigate('/dashboard');
      } else {
        // User doesn't have a shop, check role
        if (currentUser.role === 'ADMIN') {
          // Admin users need to set up their shop first
          console.log('Redirecting ADMIN to shop setup...');
          navigate('/shop-setup');
        } else {
          // Staff users without shop need to wait for admin assignment
          console.log('Redirecting staff to unauthorized...');
          navigate('/unauthorized');
        }
      }
    }
  }, [currentUser, shopId, navigate]);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setIsLoading(true);

    try {
      const result = await signIn(email, password);
      
      if (result.success) {
        toast({
          title: 'Welcome back!',
          description: 'You have successfully signed in.',
        });
        await refreshUser();
      } else {
        toast({
          title: 'Sign in failed',
          description: result.error,
          variant: 'destructive',
        });
      }
    } catch (error) {
      toast({
        title: 'Sign in failed',
        description: 'An unexpected error occurred. Please try again.',
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleGoogleLogin = async () => {
    console.log('Google login button clicked');
    setIsLoading(true);
    
    try {
      const result = await signInWithGoogle();
      
      if (result.success) {
        toast({
          title: 'Welcome!',
          description: 'You have successfully signed in with Google.',
        });
        // User doc may have been created just now; refresh so redirect runs with correct currentUser/shopId
        await refreshUser();
      } else {
        console.log('Google login failed:', result.error);
        
        // Check if it's a network-related issue vs user cancellation
        const isNetworkError = result.error.toLowerCase().includes('network') || 
                              result.error.toLowerCase().includes('connection') ||
                              result.error.toLowerCase().includes('internet');
        
        const isUserAction = result.error.toLowerCase().includes('cancelled') ||
                            result.error.toLowerCase().includes('blocked');
        
        if (isNetworkError) {
          // Network errors get destructive toast
          toast({
            title: 'Connection Error',
            description: result.error,
            variant: 'destructive',
          });
        } else if (isUserAction) {
          // User actions get neutral toast
          toast({
            title: 'Sign-in Interrupted',
            description: result.error,
            variant: 'default',
          });
        } else {
          // Other errors get destructive toast
          toast({
            title: 'Google sign-in failed',
            description: result.error,
            variant: 'destructive',
          });
        }
      }
    } catch (error) {
      console.error('Unexpected error in Google login:', error);
      toast({
        title: 'Google sign-in failed',
        description: 'An unexpected error occurred. Please try again or use email sign-in.',
        variant: 'destructive',
      });
    } finally {
      setIsLoading(false);
    }
  };

  const handleForgotPassword = async () => {
    if (!email) {
      toast({
        title: 'Email required',
        description: 'Please enter your email address to reset password.',
        variant: 'destructive',
      });
      return;
    }

    const result = await resetPassword(email);
    
    if (result.success) {
      toast({
        title: 'Password reset email sent',
        description: 'Check your email for password reset instructions.',
      });
    } else {
      toast({
        title: 'Password reset failed',
        description: result.error,
        variant: 'destructive',
      });
    }
  };

  return (
    <div className="min-h-screen flex">
      {/* Left side - Branding */}
      <div className="hidden lg:flex lg:w-1/2 bg-sidebar items-center justify-center p-12">
        <div className="max-w-md text-center animate-fade-in">
          <div className="flex justify-center mb-8">
            <div className="h-20 w-20 rounded-2xl bg-sidebar-primary flex items-center justify-center shadow-glow overflow-hidden">
              <img 
                src={logo} 
                alt="TailorFlow Logo" 
                className="h-16 w-16 object-contain"
              />
            </div>
          </div>
          <h1 className="text-4xl font-bold text-sidebar-foreground mb-4">TailorFlow</h1>
          <p className="text-lg text-sidebar-foreground/70 mb-8">
            The complete solution for managing your tailoring business. Track customers, orders, measurements, and payments all in one place.
          </p>
          <div className="grid grid-cols-2 gap-4 text-left">
            {[
              'Customer Management',
              'Order Tracking',
              'Measurement Records',
              'Payment Processing',
            ].map((feature) => (
              <div key={feature} className="flex items-center gap-2 text-sidebar-foreground/80">
                <div className="h-2 w-2 rounded-full bg-sidebar-primary" />
                <span className="text-sm">{feature}</span>
              </div>
            ))}
          </div>
        </div>
      </div>

      {/* Right side - Login Form */}
      <div className="flex-1 flex items-center justify-center p-6 lg:p-12">
        <div className="w-full max-w-md animate-slide-up">
          {/* Mobile logo */}
          <div className="flex lg:hidden justify-center mb-8">
            <div className="flex items-center gap-3">
              <div className="h-12 w-12 rounded-xl bg-primary flex items-center justify-center overflow-hidden">
                <img 
                  src={logo} 
                  alt="TailorFlow Logo" 
                  className="h-10 w-10 object-contain"
                />
              </div>
              <span className="text-2xl font-bold">TailorFlow</span>
            </div>
          </div>

          <Card className="border-0 shadow-soft lg:shadow-none">
            <CardHeader className="space-y-1 text-center lg:text-left">
              <CardTitle className="text-2xl font-bold">Welcome back</CardTitle>
              <CardDescription>
                Sign in to manage customers, orders, and payments in one place.
              </CardDescription>
            </CardHeader>
            <CardContent>
              <form onSubmit={handleSubmit} className="space-y-4">
                <div className="space-y-2">
                  <Label htmlFor="email">Email</Label>
                  <div className="relative">
                    <Mail className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
                    <Input
                      id="email"
                      type="email"
                      placeholder="john@example.com"
                      value={email}
                      onChange={(e) => setEmail(e.target.value)}
                      className="pl-10"
                      required
                    />
                  </div>
                </div>

                <div className="space-y-2">
                  <div className="flex items-center justify-between">
                    <Label htmlFor="password">Password</Label>
                    <Link to="/forgot-password" className="text-sm text-primary hover:underline">
                      Forgot password?
                    </Link>
                  </div>
                  <div className="relative">
                    <Lock className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
                    <Input
                      id="password"
                      type={showPassword ? 'text' : 'password'}
                      placeholder="••••••••"
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      className="pl-10 pr-10"
                      required
                    />
                    <button
                      type="button"
                      onClick={() => setShowPassword(!showPassword)}
                      className="absolute right-3 top-1/2 -translate-y-1/2 text-muted-foreground hover:text-foreground"
                    >
                      {showPassword ? <EyeOff className="h-4 w-4" /> : <Eye className="h-4 w-4" />}
                    </button>
                  </div>
                </div>

                <Button type="submit" className="w-full" size="lg" disabled={isLoading}>
                  {isLoading ? 'Signing in...' : 'Sign in'}
                </Button>
              </form>

              <p className="text-center text-sm text-muted-foreground mt-4">
                <Link to="/forgot-password" className="text-primary hover:underline">
                  Forgot your password?
                </Link>
              </p>

              <div className="relative my-6">
                <div className="absolute inset-0 flex items-center">
                  <div className="w-full border-t border-border" />
                </div>
                <div className="relative flex justify-center text-xs uppercase">
                  <span className="bg-card px-2 text-muted-foreground">Or continue with</span>
                </div>
              </div>

              <Button
                type="button"
                variant="outline"
                className="w-full"
                size="lg"
                onClick={handleGoogleLogin}
                disabled={isLoading}
              >
                <svg className="h-4 w-4 mr-2" viewBox="0 0 24 24">
                  <path
                    fill="currentColor"
                    d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                  />
                  <path
                    fill="currentColor"
                    d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                  />
                  <path
                    fill="currentColor"
                    d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                  />
                  <path
                    fill="currentColor"
                    d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                  />
                </svg>
                Continue with Google
              </Button>

              <div className="text-center">
                <p className="text-sm text-muted-foreground">
                  Don't have an account?{' '}
                  <Link to="/register" className="text-primary font-medium hover:underline">
                    Sign up
                  </Link>
                </p>
              </div>
            </CardContent>
          </Card>
        </div>
      </div>
    </div>
  );
}
